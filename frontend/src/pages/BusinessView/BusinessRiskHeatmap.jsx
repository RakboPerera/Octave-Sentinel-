// ─── HEAT CELL ───────────────────────────────────────────────────────────────
function HeatCell({ cell, domain, category, selected, onClick }) {
  const { criticals = 0, highs = 0, mediums = 0, exposure = 0 } = cell;
  const total = criticals + highs + mediums;
  const primaryAgents = domain.agentsPrimary || [];
  const theoreticallyCovered = primaryAgents.some(a => AGENT_TO_RISK_CATEGORY[a] === category);

  let bg, fg;
  if (total === 0) {
    bg = theoreticallyCovered ? 'rgba(11,191,122,0.12)' : 'rgba(200,200,200,0.10)';
    fg = theoreticallyCovered ? '#0BBF7A' : 'var(--color-text-3)';
  } else if (criticals > 0) {
    const intensity = Math.min(0.9, 0.35 + criticals * 0.18);
    bg = `rgba(196,30,58,${intensity})`;
    fg = 'white';
  } else if (highs > 0) {
    const intensity = Math.min(0.85, 0.3 + highs * 0.14);
    bg = `rgba(180,83,9,${intensity})`;
    fg = 'white';
  } else {
    bg = 'rgba(202,138,4,0.35)';
    fg = '#713F12';
  }

  const border = selected ? '2px solid #F5B841' : '1px solid rgba(0,0,0,0.05)';

  return (
    <button
      onClick={onClick}
      title={`${domain.label} · ${category}${exposure > 0 ? '\nExposure: ' + formatLkr(exposure) : ''}`}
      style={{
        width: 72, height: 52, borderRadius: 6, cursor: total > 0 ? 'pointer' : 'default',
        background: bg, color: fg, border,
        fontFamily: 'var(--font-display)',
        display: 'flex', flexDirection: 'column', alignItems: 'center', justifyContent: 'center',
        padding: 0, transition: 'transform 0.12s',
        boxShadow: selected ? '0 0 0 3px rgba(245,184,65,0.2)' : 'none',
      }}
      disabled={total === 0}
    >
      {total === 0 ? (
        <span style={{ fontSize: 13, opacity: 0.5 }}>—</span>
      ) : (
        <>
          {criticals > 0 && (
            <div style={{ fontSize: 11, fontWeight: 800, lineHeight: 1.1 }}>
              {criticals} <span style={{ fontSize: 9, fontWeight: 700, opacity: 0.85 }}>crit</span>
            </div>
          )}
          {highs > 0 && (
            <div style={{ fontSize: 10.5, fontWeight: 700, lineHeight: 1.1, opacity: 0.95 }}>
              {highs} <span style={{ fontSize: 9, opacity: 0.85 }}>high</span>
            </div>
          )}
          {criticals === 0 && highs === 0 && mediums > 0 && (
            <div style={{ fontSize: 10.5, fontWeight: 700 }}>
              {mediums} <span style={{ fontSize: 9, opacity: 0.8 }}>med</span>
            </div>
          )}
        </>
      )}
    </button>
  );
}

