// ─── BUSINESS RISK HEATMAP (REDESIGNED) ──────────────────────────────────────
// Two-column layout: matrix on the left, selected-cell case list + inline
// explainability on the right. Clicking a cell selects it; clicking a case
// in the right-hand list loads its explainability underneath.

// Agent → Risk Category mapping (structural, not data)
const AGENT_TO_RISK_CATEGORY = {
  credit:         'Credit',
  transaction:    'Financial Crime',
  suspense:       'Operational',
  kyc:            'Compliance',
  controls:       'Operational',
  digital:        'Cyber & Fraud',
  trade:          'Financial Crime',
  insider:        'Conduct',
  mje:            'Financial Reporting',
  capital:        'Capital & Liquidity',
  balance:        'Capital & Liquidity',
  wealth:         'Conduct',
  collateral:     'Credit',
  connectedParty: 'Credit',
  alm:            'Market',
  thirdParty:     'Operational',
  accessRights:   'Cyber & Fraud',
  conduct:        'Conduct',
};

const RISK_CATEGORIES = [
  'Credit',
  'Market',
  'Capital & Liquidity',
  'Operational',
  'Financial Crime',
  'Compliance',
  'Cyber & Fraud',
  'Conduct',
  'Financial Reporting',
];

const CATEGORY_DESCRIPTIONS = {
  'Credit':              'Loan-book staging, collateral integrity, single-obligor and connected-party limits.',
  'Market':              'Interest rate risk in the banking book, ALM gaps, duration and sensitivity.',
  'Capital & Liquidity': 'Basel III ratios — Tier 1, Total CAR, LCR, NSFR — and balance-sheet drivers.',
  'Operational':         'Reconciliation ageing, branch controls, vendor concentration, outsourcing risk.',
  'Financial Crime':     'AML structuring, TBML, over-invoicing, duplicate LCs, transaction surveillance.',
  'Compliance':          'CDD/KYC integrity, sanctions hits, PEP EDD currency, FATF exposure.',
  'Cyber & Fraud':       'Account takeover, session anomalies, biometric deviation, access-rights drift.',
  'Conduct':             'Staff insider-risk behaviour, wealth mis-selling, recurring conduct events.',
  'Financial Reporting': 'Manual journal integrity, tax position testing, maker-checker breaches.',
};

export default function BusinessRiskHeatmap() {
  const navigate = useNavigate();
  const findings = useAllFindings();
  const { state } = useApp();
  const caseWorkbench = state.caseWorkbench || {};

  // Merge static cases with any generated by live agent runs so ageing
  // metrics below the matrix see the full population.
  const allCases = useMemo(() => {
    const generated = state.cases || [];
    const demoCases = fixtureCases(state); // fixtures only when the engine has no cases of its own
    const byId = new Set(demoCases.map(c => c.id));
    return [...demoCases, ...generated.filter(c => !byId.has(c.id))];
  }, [state.cases, state.demoMode]);

  const [selectedCell, setSelectedCell] = useState(null); // { domainId, category }
  const [selectedFindingIdx, setSelectedFindingIdx] = useState(0);

  // Build the matrix: (domain, category) → { criticals, highs, exposure, findings[] }
  const matrix = useMemo(() => {
    const m = {};
    for (const d of DOMAINS) {
      m[d.id] = {};
      for (const c of RISK_CATEGORIES) m[d.id][c] = { criticals: 0, highs: 0, mediums: 0, exposure: 0, findings: [] };
    }
    for (const f of findings) {
      const cat = AGENT_TO_RISK_CATEGORY[f.agentId] || 'Operational';
      const sev = (f.severity || 'medium').toLowerCase();
      const exp = f.finding?.affected_exposure_lkr || f.finding?.affected_balance_lkr || f.finding?.exposure_lkr || 0;
      for (const dom of f.domainTags || []) {
        const cell = m[dom]?.[cat];
        if (!cell) continue;
        if (sev === 'critical') cell.criticals++;
        else if (sev === 'high') cell.highs++;
        else if (sev === 'medium') cell.mediums++;
        cell.exposure += exp;
        cell.findings.push(f);
      }
    }
    return m;
  }, [findings]);

  // Entity-deduped flagged exposure — the same basis as Reports / Bank Position,
  // so the heatmap headline reconciles with them (a finding-summed total would
  // double-count an entity flagged by several agents).
  const entityExposure = useMemo(() => dedupedExposureBreakdown(findings).total, [findings]);

  const selectedFindings = selectedCell ? matrix[selectedCell.domainId]?.[selectedCell.category]?.findings || [] : [];
  const selectedFinding = selectedFindings[selectedFindingIdx];

  function onCellClick(domainId, category) {
    setSelectedCell({ domainId, category });
    setSelectedFindingIdx(0);
  }

  // Full-page subview when a cell is selected; otherwise full-width matrix.
  if (selectedCell) {
    return (
      <CellDrilldownPage
        cell={matrix[selectedCell.domainId][selectedCell.category]}
        domain={getDomain(selectedCell.domainId)}
        category={selectedCell.category}
        selectedFindings={selectedFindings}
        selectedFindingIdx={selectedFindingIdx}
        onSelectFinding={setSelectedFindingIdx}
        selectedFinding={selectedFinding}
        onBack={() => { setSelectedCell(null); setSelectedFindingIdx(0); }}
      />
    );
  }

  return (
    <div style={{ maxWidth: 1500, display: 'flex', flexDirection: 'column', gap: 14 }}>
      {/* 0. Live cross-agent orchestrator — only renders content once ≥2 agents have run. */}
      <OrchestratorPanel />

      {/* 1. Heatmap summary — derived metrics from the matrix below */}
      <HeatmapInsights
        matrix={matrix}
        entityExposure={entityExposure}
        onPickCell={onCellClick}
        onPickDomain={(id) => navigate(`/business-view/${id}`)}
      />

      {/* 2. The heatmap itself */}
      <Header />
      <Legend />

      <div style={{ background: 'var(--color-surface)', border: '1px solid var(--color-border)', borderRadius: 'var(--radius-lg)', padding: 14, overflowX: 'auto' }}>
        <table style={{ width: '100%', borderCollapse: 'separate', borderSpacing: 0, fontSize: 11 }}>
          <thead>
            <tr>
              <th style={{ ...thBase, position: 'sticky', left: 0, background: 'var(--color-surface)', zIndex: 2, minWidth: 200 }}>Domain</th>
              {RISK_CATEGORIES.map(c => (
                <th key={c} style={{ ...thBase, textAlign: 'center', minWidth: 90 }}>
                  <div style={{ display: 'inline-flex', alignItems: 'center', justifyContent: 'center' }}>
                    <div style={{ fontWeight: 800, color: 'var(--color-text-2)', fontSize: 9.5 }}>{c}</div>
                    <InfoHint text={CATEGORY_DESCRIPTIONS[c]} title={c} size={11} align="center" />
                  </div>
                </th>
              ))}
            </tr>
          </thead>
          <tbody>
            {DOMAINS.map(d => (
              <tr key={d.id}>
                <td
                  style={{ ...tdBase, position: 'sticky', left: 0, background: 'var(--color-surface)', zIndex: 1, minWidth: 200 }}
                >
                  <button
                    onClick={() => navigate(`/business-view/${d.id}`)}
                    style={{ background: 'none', border: 'none', cursor: 'pointer', padding: 0, textAlign: 'left', width: '100%' }}
                    title={`Open ${d.label} deep-dive`}
                  >
                    <div style={{ fontWeight: 800, color: 'var(--color-text)', fontSize: 12 }}>{d.label}</div>
                    <div style={{ fontSize: 9.5, fontWeight: 700, color: 'var(--color-text-3)', textTransform: 'uppercase', letterSpacing: '0.05em' }}>{d.ownerRole}</div>
                  </button>
                </td>
                {RISK_CATEGORIES.map(c => {
                  const cell = matrix[d.id][c];
                  return (
                    <td key={c} style={{ ...tdBase, padding: 3, textAlign: 'center' }}>
                      <HeatCell
                        cell={cell}
                        domain={d}
                        category={c}
                        selected={false}
                        onClick={() => onCellClick(d.id, c)}
                      />
                    </td>
                  );
                })}
              </tr>
            ))}
          </tbody>
        </table>
      </div>

      {/* 3. Case lifecycle — derived from cases + stateHistory + workbench */}
      <CaseLifecycleInsights
        cases={allCases}
        caseWorkbench={caseWorkbench}
        navigate={navigate}
      />
    </div>
  );
}

